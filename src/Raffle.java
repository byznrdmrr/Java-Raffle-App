    public void raffle() {
        try(BufferedReader reader = new BufferedReader(new InputStreamReader(new FileInputStream(filePath),"UTF8"))) {

            String person;
            while ((person = reader.readLine()) != null) {
                attendees.add(person);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Raffle.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(Raffle.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Raffle.class.getName()).log(Level.SEVERE, null, ex);
        }
        while (winners.size() != 5) {
            Random random = new Random();
            int winnerIndex = random.nextInt(attendees.size());
            winners.add(attendees.get(winnerIndex));
        }

    }
    public void applause(){
        try {
            AudioInputStream stream = AudioSystem.getAudioInputStream(new File("alkış.wav"));
            Clip clip = AudioSystem.getClip();
            clip.open(stream);
            clip.start();
        } catch (UnsupportedAudioFileException ex) {
            Logger.getLogger(Raffle.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Raffle.class.getName()).log(Level.SEVERE, null, ex);
        } catch (LineUnavailableException ex) {
            Logger.getLogger(Raffle.class.getName()).log(Level.SEVERE, null, ex);
        }
   }
